# ===================== ПАРСИНГ ГОРОДА =====================

# Базовый паттерн кириллического названия (одно или несколько слов с дефисом)
_NAME = r'([А-ЯЁа-яё][А-ЯЁа-яё\-]+(?:[ \-][А-ЯЁа-яё][А-ЯЁа-яё\-]+)*)'

# Стоп-слова — после них название города заканчивается
_STOP_RE = re.compile(
    r'\s+\b(р[-\s]?н|район|обл(?:асть)?|кра[йя]|республик[аи]|автономн|округ|'
    r'улица|шоссе|переулок|проспект|тракт|бульвар|набережная|площадь|'
    r'\bул\b|\bш\b|\bпер\b|\bпр[-\s]?[дт]\b|\bпр-кт\b|\bнаб\b|\bпл\b|'
    r'мкр|кв[-\s]?л|тер|промзон|промышленн|массив|'
    r'спецгородок|спецпоселок|поселение|кп[-\d]|стр\b|зд\b)\b.*',
    re.IGNORECASE
)

# Слова-регионы и технические слова — не могут быть городом
_BAD_WORDS = re.compile(
    r'^(адыгея|башкортостан|бурятия|дагестан|ингушетия|калмыкия|карелия|'
    r'мордовия|якутия|татарстан|удмуртия|хакасия|чечня|чувашия|коми|тыва|'
    r'алтайский|краснодарский|красноярский|приморский|ставропольский|'
    r'хабаровский|камчатский|забайкальский|пермский|московская|ленинградская|'
    r'свердловская|челябинская|тюменская|новосибирская|омская|иркутская|'
    r'кемеровская|воронежская|волгоградская|ростовская|саратовская|'
    r'самарская|нижегородская|кировская|ярославская|тверская|тульская|'
    r'рязанская|орловская|брянская|смоленская|калужская|тамбовская|'
    r'липецкая|белгородская|курская|владимирская|ивановская|костромская|'
    r'вологодская|архангельская|мурманская|псковская|новгородская|'
    r'калининградская|астраханская|амурская|сахалинская|магаданская|'
    r'еврейская|ульяновская|пензенская|оренбургская|курганская|'
    r'томская|россия|область|край|республика|округ|'
    # технические слова, не города
    r'рабочий|поселок|посёлок|городской|сельского|городского|'
    r'спецгородок|спецпоселок|промзона|промышленный|промышленная|'
    r'водстрой|труда|им|улица|восток|запад|'
    r'деревня|станция|разъезд|выселки|кордон)$',
    re.IGNORECASE
)


def _clean_name(raw: str) -> str:
    """Финальная очистка кандидата названия населённого пункта."""
    if not raw:
        return ''
    raw = raw.strip()
    # Убираем скобки и всё внутри
    raw = re.sub(r'\(.*?\)', '', raw).strip()
    # Отрезаем по запятой или точке с запятой
    raw = re.split(r'[,;]', raw)[0].strip()
    # Убираем хвост от стоп-слов
    raw = _STOP_RE.sub('', raw).strip()
    # Убираем цифры и мусор в конце
    raw = re.sub(r'[\s\d\.\-/\\«»"№]+$', '', raw).strip()
    # Слишком короткое — не город
    if len(raw) <= 2:
        return ''
    # Начинается с цифры — не город
    if raw and raw[0].isdigit():
        return ''
    # Слово из чёрного списка
    if _BAD_WORDS.match(raw):
        return ''
    return raw


def extract_city(address: str) -> str:
    """
    Извлекает название населённого пункта из строки адреса.
    Поддерживает множество форматов российских адресов ФСИН.
    """
    if not address or str(address).strip() in ('', 'nan'):
        return ''

    # --- Шаг 1: нормализация ---
    text = str(address).strip()
    # Убираем индекс в начале (5-6 цифр)
    text = re.sub(r'^\s*\d{5,6}\s*[-–,.\s]*', '', text).strip()
    # Убираем "Россия" в начале
    text = re.sub(r'^Россия\s*[,.]?\s*', '', text, flags=re.IGNORECASE).strip()
    # Убираем "Республика ХХХ" в самом начале (до первой запятой)
    text = re.sub(
        r'^Республика\s+[А-ЯЁа-яё\s\-\(\)]+?,\s*',
        '', text, flags=re.IGNORECASE
    ).strip()
    # Убираем название области/края/региона в начале (до первой запятой)
    # "Тульская область, ...", "Мурманская области г.Оленегорск"
    text = re.sub(
        r'^[А-ЯЁа-яё][а-яё]+(?:ская|ская|ской|ского|ский|ский|кий|'
        r'жский|зский|йский)\s+(?:область|обл\.?|область\.?|области|'
        r'край|края|АОБЛ|автономная\s+область)\s*[,.]?\s*',
        '', text, flags=re.IGNORECASE
    ).strip()
    # Нормализуем множественные пробелы
    text = re.sub(r'\s+', ' ', text).strip()

    if not text:
        return ''

    # --- Шаг 2: многословные типы н.п. (длинные — ПЕРВЫМИ) ---

    # "поселок городского типа Название"
    m = re.search(
        r'(?:посёлок|поселок)\s+городского\s+типа\s+' + _NAME,
        text, re.IGNORECASE
    )
    if m:
        c = _clean_name(m.group(1))
        if c: return c

    # "рабочий посёлок Название"
    m = re.search(
        r'рабочий\s+(?:посёлок|поселок)\s+' + _NAME,
        text, re.IGNORECASE
    )
    if m:
        c = _clean_name(m.group(1))
        if c: return c

    # "городской посёлок Название"
    m = re.search(
        r'городской\s+(?:посёлок|поселок)\s+' + _NAME,
        text, re.IGNORECASE
    )
    if m:
        c = _clean_name(m.group(1))
        if c: return c

    # "сельский поселок Название" / "поселок сельского типа Название"
    m = re.search(
        r'(?:сельский\s+(?:посёлок|поселок)|(?:посёлок|поселок)\s+сельского\s+типа)\s+' + _NAME,
        text, re.IGNORECASE
    )
    if m:
        c = _clean_name(m.group(1))
        if c: return c

    # --- Шаг 3: капслок-стиль реестров "Название Город/Поселок" ---
    # Захватываем только само название, без слова-типа
    # "Кострома Город", "Белово Город", "Шексна Рабочий поселок"
    m = re.search(
        _NAME + r'\s+(?:Город|ГОРОД)\b',
        text
    )
    if m:
        c = _clean_name(m.group(1))
        if c: return c

    # "Название Рабочий поселок" (капслок)
    m = re.search(
        _NAME + r'\s+Рабочий\s+[Пп]оселок\b',
        text
    )
    if m:
        c = _clean_name(m.group(1))
        if c: return c

    # "Название Поселок/ПОСЕЛОК" (капслок) — только одно слово "Поселок"
    # Исключаем "городского/сельского" после — они уже обработаны выше
    m = re.search(
        _NAME + r'\s+(?:Поселок|ПОСЕЛОК|Посёлок)\b(?!\s+городского|\s+сельского)',
        text
    )
    if m:
        c = _clean_name(m.group(1))
        if c: return c

    # Специальный случай "Название Поселок городского типа" (капслок) — берём Название
    m = re.search(
        _NAME + r'\s+[Пп]оселок\s+городского\s+типа\b',
        text
    )
    if m:
        c = _clean_name(m.group(1))
        if c: return c

    # Специальный случай "Название Поселок сельского типа" (капслок) — берём Название
    m = re.search(
        _NAME + r'\s+[Пп]оселок\s+сельского\s+типа\b',
        text
    )
    if m:
        c = _clean_name(m.group(1))
        if c: return c

    # "Название Деревня/ДЕРЕВНЯ" (капслок)
    m = re.search(_NAME + r'\s+(?:Деревня|ДЕРЕВНЯ)\b', text)
    if m:
        c = _clean_name(m.group(1))
        if c: return c

    # "Название Село/СЕЛО" (капслок)
    m = re.search(_NAME + r'\s+(?:Село|СЕЛО)\b', text)
    if m:
        c = _clean_name(m.group(1))
        if c: return c

    # --- Шаг 4: "Название г" / "Название город" (в конце токена) ---
    # "Архангельск г", "Находка г", "Биробиджан г"
    m = re.search(
        _NAME + r'\s+(?:г|город)\b(?!\w)',
        text, re.IGNORECASE
    )
    if m:
        c = _clean_name(m.group(1))
        if c and not _BAD_WORDS.match(c):
            return c

    # --- Шаг 5: стандартные паттерны "тип_нп. Название" ---
    patterns = [
        # п.г.т. и пгт — обязательно ДО "п."
        r'(?:п\.?\s*г\.?\s*т\.?|пгт\.?)\s+' + _NAME,
        # р.п. — обязательно ДО "п."
        r'р\.?\s*п\.?\s+' + _NAME,
        # рп (без точек, слитно с пробелом) — "рп Эльбан"
        r'(?<!\w)рп\s+' + _NAME,
        # пст. (погост/починок)
        r'пст\.?\s+' + _NAME,
        # сп. (сельское поселение) — "сп. Шерстки"
        r'(?<!\w)сп\.?\s+' + _NAME,
        # п/о (почтовое отделение) — игнорируем, не город
        # посёлок / пос. / пос
        r'(?:посёлок|поселок|пос)\.?\s+' + _NAME,
        # город / г. (с пробелом — обязательно)
        r'(?<!\w)(?:город|г)\.?\s+' + _NAME,
        # село / с. (с пробелом)
        r'(?<!\w)(?:село|с)\.?\s+' + _NAME,
        # деревня / д. (только если следует заглавная буква, не цифра)
        r'(?<!\w)(?:деревня|д)\.?\s+(?=[А-ЯЁ])' + _NAME,
        # хутор
        r'\bхутор\s+' + _NAME,
        # станица
        r'\bстаница\s+' + _NAME,
        # аул
        r'\bаул\s+' + _NAME,
        # п. — самый короткий, в последнюю очередь
        r'(?<!\w)п\.?\s+' + _NAME,
    ]

    for pat in patterns:
        m = re.search(pat, text, re.IGNORECASE)
        if m:
            c = _clean_name(m.group(1))
            if c:
                return c

    # --- Шаг 5б: формат "Название тип_в_конце" (тип после названия) ---
    # "Талаги п", "Данилово д", "Колосовка п", "Архангельск г", "Онега г"
    # "Возжаевка с", "Тахтамыгда с", "Чернянка п", "Ерцево п", "Пукса п"
    end_patterns = [
        # "Название пгт" в конце
        _NAME + r'\s+(?:пгт|пгт\.)\s*$',
        # "Название г" в конце (без запятой после)
        _NAME + r'\s+г\s*$',
        # "Название г" перед запятой
        _NAME + r'\s+г\s*,',
        # "Название с" в конце (село)
        _NAME + r'\s+с\s*$',
        # "Название с" перед запятой
        _NAME + r'\s+с\s*,',
        # "Название п" в конце
        _NAME + r'\s+п\s*$',
        # "Название п" перед запятой
        _NAME + r'\s+п\s*,',
        # "Название д" в конце (деревня)
        _NAME + r'\s+д\s*$',
        # "Название д" перед запятой
        _NAME + r'\s+д\s*,',
        # "Название рп" в конце
        _NAME + r'\s+рп\s*$',
    ]

    for pat in end_patterns:
        m = re.search(pat, text, re.IGNORECASE)
        if m:
            c = _clean_name(m.group(1))
            if c:
                return c

    # --- Шаг 6: форматы без пробела ---
    # "г.Иркутск", "г.Краснодар" — слитно
    m = re.search(r'(?<!\w)г\.([А-ЯЁ][а-яёА-ЯЁ\-]+(?:\s[А-ЯЁ][а-яёА-ЯЁ\-]+)?)', text)
    if m:
        c = _clean_name(m.group(1))
        if c: return c

    # "п.Пуксинка", "п.Сосьва" — слитно
    m = re.search(r'(?<!\w)п\.([А-ЯЁ][а-яёА-ЯЁ\-]+(?:\s[А-ЯЁ][а-яёА-ЯЁ\-]+)?)', text)
    if m:
        c = _clean_name(m.group(1))
        if c: return c

    # --- Шаг 7: адрес начинается сразу с "г. Город" ---
    m = re.match(r'(?:г\.?\s*)([А-ЯЁ][а-яёА-ЯЁ\-]+(?:[\s\-][А-ЯЁ][а-яёА-ЯЁ\-]+)?)', text)
    if m:
        c = _clean_name(m.group(1))
        if c: return c

    return ''


# ===================== ИЗВЛЕЧЕНИЕ ИНДЕКСА =====================

def extract_zip(address: str) -> str:
    """Извлекает почтовый индекс (6 цифр) из адреса."""
    if not address or str(address).strip() in ('', 'nan'):
        return ''
    m = re.search(r'\b(\d{6})\b', str(address))
    return m.group(1) if m else ''


def remove_zip(address: str) -> str:
    """Удаляет почтовый индекс (6 цифр) из адреса и очищает лишние пробелы/запятые."""
    if not address or str(address).strip() in ('', 'nan'):
        return ''

    # Превращаем в строку на случай, если пришло что-то другое
    address_str = str(address)

    # 1. Заменяем 6 цифр (индекс) на пустую строку
    # Шаблон \b\d{6}\b ищет ровно 6 цифр, стоящих отдельно
    result = re.sub(r'\b\d{6}\b', '', address_str)

    # 2. Очистка: убираем лишние пробелы и повторяющиеся запятые,
    # которые могли остаться после удаления индекса
    result = re.sub(r'\s{2,}', ' ', result)  # Двойные пробелы -> один
    result = re.sub(r',\s*,', ',', result)  # Запятые подряд -> одна

    return result.strip(', ')  # Убираем пробелы и запятые по краям
